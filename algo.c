#include "push_swap.h"

void	sort_three(t_ps *ps)
{
	find_max_min_a(ps);
	if (ps->a[0] == ps->amax)
		ra(ps, 1);
	else if (ps->a[1] == ps->amax)
		rra(ps, 1);
	if (ps->a[0] > ps->a[1])
		sa(ps, 1);
}
